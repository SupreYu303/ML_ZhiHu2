"""
煤巷顶板位移机器学习预测与工程校核 —— 公共路径配置
======================================================
使用 pathlib.Path，自动定位项目根目录，兼容 Windows。
"""

from pathlib import Path


# ---------------------------------------------------------------------------
# 自动定位项目根目录：此文件位于 <PROJECT_ROOT>/src/00_config.py
# ---------------------------------------------------------------------------
PROJECT_ROOT = Path(__file__).resolve().parent.parent

# ---------------------------------------------------------------------------
# 数据目录
# ---------------------------------------------------------------------------
DATA_RAW_DIR       = PROJECT_ROOT / "data" / "raw"
DATA_PROCESSED_DIR = PROJECT_ROOT / "data" / "processed"

# ---------------------------------------------------------------------------
# 输出根目录
# ---------------------------------------------------------------------------
OUTPUT_DIR  = PROJECT_ROOT / "outputs"

# ---------------------------------------------------------------------------
# 输出子目录
# ---------------------------------------------------------------------------
FIGURE_DIR = OUTPUT_DIR / "figures"
TABLE_DIR  = OUTPUT_DIR / "tables"
MODEL_DIR  = OUTPUT_DIR / "models"
REPORT_DIR = OUTPUT_DIR / "reports"

# ---------------------------------------------------------------------------
# 辅助函数：确保所有目录存在
# ---------------------------------------------------------------------------
def ensure_dirs() -> None:
    """创建本项目所需的所有输出/数据目录（若已存在则跳过）。"""
    all_dirs = [
        DATA_RAW_DIR,
        DATA_PROCESSED_DIR,
        FIGURE_DIR,
        TABLE_DIR,
        MODEL_DIR,
        REPORT_DIR,
    ]
    for d in all_dirs:
        d.mkdir(parents=True, exist_ok=True)


# ---------------------------------------------------------------------------
# 自检入口
# ---------------------------------------------------------------------------
if __name__ == "__main__":
    print(f"项目根目录: {PROJECT_ROOT}")
    print(f"数据原始目录: {DATA_RAW_DIR}")
    print(f"数据预处理目录: {DATA_PROCESSED_DIR}")
    print(f"输出根目录: {OUTPUT_DIR}")
    print(f"图表目录: {FIGURE_DIR}")
    print(f"表格目录: {TABLE_DIR}")
    print(f"模型目录: {MODEL_DIR}")
    print(f"报告目录: {REPORT_DIR}")
    ensure_dirs()
    print("所有目录已就绪。")